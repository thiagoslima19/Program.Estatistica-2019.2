financiamento<-function(n){
  if(n==1)
    return((1200*(1+2/100))-150)
  if(((1+2/100)*financiamento(n-1)-150)<=0)
    return(0)
  return((1+2/100)*financiamento(n-1)-150)
}

financiamento(2)
