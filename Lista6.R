posmaximo<-function(v){
  n=length(v)
  if(n==1)
    return(1)
  w=v[1:(n-1)]
  pos=posmaximo(w)
  if(v[n]>v[pos])
    return(n)
  return(pos)
}
r=runif(6,0,1)
posmaximo(r)
